# ANSI последовательности для цветного вывода в терминале
ANSI_COLORS = {
    'RED'       : '\033[91m',
    'GREEN'     : '\033[92m',
    'YELLOW'    : '\033[93m',
    'CYAN'      : '\033[94m',
    'BLUE'      : '\033[94m',
    'ENDC'      : '\033[0m',
}
#------------------------------------------------------------------------------
#
# print_ok_box: выводит сообщение в зелёном цвете с префиксом [  OK  ]
#
def print_ok_box(message):
    print(f"[{ANSI_COLORS['GREEN']}  OK  {ANSI_COLORS['ENDC']}] {message}")
#------------------------------------------------------------------------------
#
# print_err_box: выводит сообщение в красном цвете с префиксом [FAILED]
#
def print_err_box(message):
    print(f"[{ANSI_COLORS['RED']}FAILED{ANSI_COLORS['ENDC']}] {message}")
#------------------------------------------------------------------------------
#
# print_warn_box: выводит сообщение в жёлтом цвете с префиксом [ WARN ]
#
def print_warn_box(message):
    print(f"[{ANSI_COLORS['YELLOW']} WARN {ANSI_COLORS['ENDC']}] {message}")
